      <span>Relocation can pose a challenge for a business. That’s why CMS’s professionals are at your service to help design and coordinate a comprehensive plan that will allow you to move your company with minimal interruption to operations.  Certified welders, engineers and quality control personnel are trained in fabrication and disassembly procedures while riggers and drivers are focused on lifting and moving your machinery then installing at your new location.  Our specialized industrial heavy haul team will provide a time and cost efficient solution helping clients determine the appropriate course of action for the transport then executing the plan and completing the move successfully to get you back in business</span>
      <strong>• Transportation:</strong>
      <span>CMS is dedicated to providing excellent total transportation services for heavy equipment hauls, rigging and machinery and oversized loads. From taking machinery apart to reassembling equipment at a new location, our trained and certified professionals will manage your move, logistics, storage and installation with care, integrity, attention to detail, and a commitment to safety.</span>
